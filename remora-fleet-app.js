import * as THREE from 'three';
import { OrbitControls } from 'three/addons/controls/OrbitControls.js';

// --- CONFIGURATION ---
const UNIT_COUNT = 100;
const HOST_SIZE = 10;
const SWARM_RADIUS = 50;

// --- STATE ---
let scene, camera, renderer, controls;
let host, remoraFleet = [];
let attachedCount = 0;
let phase = 'MANEUVERING';

const hudUnits = document.getElementById('unit-count');
const hudAttached = document.getElementById('attached-count');
const hudPhase = document.getElementById('phase-indicator');

// --- DATA PLANE (gRPC / BINARY STREAM) ---
// In a production scenario, this would connect to the voxel_stream_engine (Port 9001)
// and receive binary Protobuf messages generated by remora_sim_server.py.
const USE_NETWORKED_SIM = true;

function connectToStream() {
    console.log("[DATA PLANE] Connecting to ScytheStreamService...");

    // For this demo, we mock the binary WebSocket/gRPC stream
    // In reality: const socket = new WebSocket('ws://localhost:9001');
    setInterval(() => {
        if (USE_NETWORKED_SIM && host && remoraFleet.length > 0) {
            // Mock receiving a binary packet from the gRPC authoritative server
            mockReceiveBinaryPacket();
        }
    }, 33); // ~30Hz
}

function mockReceiveBinaryPacket() {
    // 1. Authoritative Host Rotation from Server
    host.rotation.y += 0.01;
    host.rotation.z += 0.005;

    let currentAttached = 0;

    // 2. Update Units based on "server" authoritative state
    remoraFleet.forEach((remora) => {
        const data = remora.userData;

        if (!data.attached) {
            // Authoritative movement toward host (Boids logic moved to server)
            const dx = host.position.x - remora.position.x;
            const dy = host.position.y - remora.position.y;
            const dz = host.position.z - remora.position.z;
            const dist = Math.sqrt(dx*dx + dy*dy + dz*dz);

            if (dist < HOST_SIZE + 0.5) {
                attachRemora(remora);
            } else {
                const speed = 0.5;
                remora.position.x += (dx / dist) * speed;
                remora.position.y += (dy / dist) * speed;
                remora.position.z += (dz / dist) * speed;

                // Look in direction of travel
                const targetRotation = new THREE.Quaternion().setFromUnitVectors(
                    new THREE.Vector3(0, 0, 1),
                    new THREE.Vector3(dx, dy, dz).normalize()
                );
                remora.quaternion.slerp(targetRotation, 0.1);
            }
        } else {
            currentAttached++;
            // Attached units follow host rotation precisely (authoritative)
            const relativePos = data.attachPoint.clone().applyEuler(host.rotation);
            remora.position.copy(relativePos);

            const worldNormal = data.attachNormal.clone().applyEuler(host.rotation);
            remora.quaternion.setFromUnitVectors(new THREE.Vector3(0, 0, 1), worldNormal);
        }
    });

    attachedCount = currentAttached;
}

// --- INITIALIZATION ---

function init() {
    // Scene setup
    scene = new THREE.Scene();
    scene.background = new THREE.Color(0x00050a);

    // Camera setup
    camera = new THREE.PerspectiveCamera(75, window.innerWidth / window.innerHeight, 0.1, 1000);
    camera.position.set(0, 30, 80);

    // Renderer setup
    renderer = new THREE.WebGLRenderer({ antialias: true });
    renderer.setSize(window.innerWidth, window.innerHeight);
    renderer.setPixelRatio(window.devicePixelRatio);
    document.body.appendChild(renderer.domElement);

    // Controls
    controls = new OrbitControls(camera, renderer.domElement);
    controls.enableDamping = true;
    controls.dampingFactor = 0.05;

    // Lighting
    const ambientLight = new THREE.AmbientLight(0x404040, 2);
    scene.add(ambientLight);

    const sunLight = new THREE.DirectionalLight(0xffffff, 3);
    sunLight.position.set(50, 50, 50);
    scene.add(sunLight);

    createStars();
    createHost();
    createRemoraFleet();

    // Start data stream
    connectToStream();

    window.addEventListener('resize', onWindowResize, false);
    animate();
}

function createStars() {
    const starGeometry = new THREE.BufferGeometry();
    const starMaterial = new THREE.PointsMaterial({ color: 0xffffff, size: 0.1 });
    const starVertices = [];
    for (let i = 0; i < 5000; i++) {
        starVertices.push((Math.random() - 0.5) * 2000, (Math.random() - 0.5) * 2000, (Math.random() - 0.5) * 2000);
    }
    starGeometry.setAttribute('position', new THREE.Float32BufferAttribute(starVertices, 3));
    scene.add(new THREE.Points(starGeometry, starMaterial));
}

function createHost() {
    const geometry = new THREE.IcosahedronGeometry(HOST_SIZE, 1);
    const material = new THREE.MeshStandardMaterial({ color: 0x444444, roughness: 0.8, metalness: 0.2, flatShading: true });
    host = new THREE.Mesh(geometry, material);
    const line = new THREE.LineSegments(new THREE.WireframeGeometry(geometry), new THREE.LineBasicMaterial({ color: 0x00f0ff, transparent: true, opacity: 0.2 }));
    host.add(line);
    scene.add(host);
}

function createRemoraFleet() {
    const geometry = new THREE.CylinderGeometry(0.8, 0.8, 0.2, 6);
    geometry.rotateX(Math.PI / 2);
    const material = new THREE.MeshStandardMaterial({ color: 0x00f0ff, emissive: 0x0066ff, emissiveIntensity: 2, metalness: 1, roughness: 0.3 });

    for (let i = 0; i < UNIT_COUNT; i++) {
        const remora = new THREE.Mesh(geometry, material.clone());
        const theta = Math.random() * Math.PI * 2;
        const phi = Math.acos((Math.random() * 2) - 1);
        const r = SWARM_RADIUS + (Math.random() * 20);
        remora.position.set(r * Math.sin(phi) * Math.cos(theta), r * Math.sin(phi) * Math.sin(theta), r * Math.cos(phi));
        remora.userData = { attached: false, attachPoint: new THREE.Vector3(), attachNormal: new THREE.Vector3() };
        remoraFleet.push(remora);
        scene.add(remora);
    }
    hudUnits.innerText = UNIT_COUNT;
}

function attachRemora(remora) {
    const data = remora.userData;
    data.attached = true;
    const normal = new THREE.Vector3().subVectors(remora.position, host.position).normalize();
    data.attachNormal.copy(normal);
    const surfacePos = normal.clone().multiplyScalar(HOST_SIZE);
    const inverseRotation = new THREE.Euler(-host.rotation.x, -host.rotation.y, -host.rotation.z, 'ZYX');
    data.attachPoint.copy(surfacePos).applyEuler(inverseRotation);
    remora.material.emissiveIntensity = 5;
    remora.material.color.setHex(0xffffff);
}

function animate() {
    requestAnimationFrame(animate);
    updateHUD();
    controls.update();
    renderer.render(scene, camera);
}

function updateHUD() {
    hudAttached.innerText = attachedCount;
    if (attachedCount === 0) {
        hudPhase.innerText = 'PHASE: INTERCEPTING TARGET...';
        hudPhase.style.color = '#ffde00';
    } else if (attachedCount < UNIT_COUNT) {
        hudPhase.innerText = 'PHASE: SECURING PAYLOAD...';
        hudPhase.style.color = '#00f0ff';
    } else {
        hudPhase.innerText = 'PHASE: TARGET NEUTRALIZED';
        hudPhase.style.color = '#00ff00';
    }
}

function onWindowResize() {
    camera.aspect = window.innerWidth / window.innerHeight;
    camera.updateProjectionMatrix();
    renderer.setSize(window.innerWidth, window.innerHeight);
}

init();
